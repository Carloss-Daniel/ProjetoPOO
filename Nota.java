package projeto;

public class Nota {
  private float ab1;
  private float ab2;


  public Nota(float ab1, float ab2){
    this.ab1 = ab1;
    this.ab2 = ab2;
  }


  public float getAb1() {
    return ab1;
  }


  public void setAb1(float ab1) {
    this.ab1 = ab1;
  }


  public float getAb2() {
    return ab2;
  }


  public void setAb2(float ab2) {
    this.ab2 = ab2;
  }

  
}
